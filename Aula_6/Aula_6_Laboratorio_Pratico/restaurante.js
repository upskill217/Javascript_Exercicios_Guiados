//prato escolhido é Pizza
let pratoEscolhido = "Pizza";
console.log(`Prato escolhido: ${pratoEscolhido}`)

switch (pratoEscolhido) {
  case "Pizza":
    console.log("Pizza de quatro queijos")
    break;
  case "Hamburger":
    console.log("Hamburger com batatas")
    break;
  case "Salada":
    console.log("Salada de vegetais frescos")
    break;
  default:
    console.log("Prato indisponível")
}

